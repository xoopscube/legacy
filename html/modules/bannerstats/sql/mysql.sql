  `htmlcode` text,
  `width` int(11) default NULL,
  `height` int(11) default NULL,
  `imptotal_allocated` int(11) NOT NULL default '0',
  `impressions_made` int(11) NOT NULL default '0',
  `clicks_made` int(11) NOT NULL default '0',
  `datestart_original` datetime default NULL,
